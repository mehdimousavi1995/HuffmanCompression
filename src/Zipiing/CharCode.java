/**
 *
 * @author Mehdi
 */
public class CharCode {
    private String value;
    private String code;

    public CharCode(String value, String code) {
        this.value = value;
        this.code = code;
    }

    /**
     * @return the value
     */
    public String getValue() {
        return value;
    }

    /**
     * @return the code
     */
    public String getCode() {
        return code;
    }
}
